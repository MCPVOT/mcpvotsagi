@dataclass
class ReasoningTask:
    """Reasoning task for DeepSeek"""
    id: str
    type: str  # 'trading', 'analysis', 'strategy', 'learning'
    context: Dict[str, Any]
    prompt: str
    priority: int
    timestamp: datetime
    result: Optional[Dict[str, Any]] = None

@dataclass
class TradingStrategy:
    """Trading strategy generated by DeepSeek"""
    id: str
    name: str
    asset_class: str  # 'precious_metals', 'crypto', 'stocks'
    confidence: float
    expected_return: float
    risk_level: float
    time_horizon: str
    entry_conditions: List[Dict[str, Any]]
    exit_conditions: List[Dict[str, Any]]
    position_sizing: Dict[str, Any]

class DeepSeekMCPServer:
    """MCP Server for DeepSeek reasoning engine"""
    
